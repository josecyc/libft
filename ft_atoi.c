#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		sign;
	int		ans;

	i = 0;
	ans = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\v' || s[i] == '\n' ||
	s[i] == '\r' || s[i] == '\f')
		i++;
	if (s[i] == '+')
		i++;
	else if (s[i] == '-')
	{
		i++;
		sign = -sign;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		ans = (ans * 10) + (s[i] - '0');
		i++;
	}
	return (ans * sign);
}
