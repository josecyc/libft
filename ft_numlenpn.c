#include "libft.h"

int		ft_numlenpn(long long int num)
{
	int		i;

	i = 0;
	if (num && num < 0)
	{
		num = -num;
		i++;
	}
	if (num == 0)
		return (1);
	if (num)
	{
		while (num > 0)
		{
			i++;
			num /= 10;
		}
	}
	return (i);
}
